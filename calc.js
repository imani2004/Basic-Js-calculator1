const num1=parseInt(prompt("Enter the first number "))
var operation=prompt("Enter the operation to be done")
const num2=parseInt(prompt("Enter the second number"))
if(operation== '+'){
     alert(num1 + num2)
}
else if (operation== '-'){
    alert(num1-num2)
}
else if(operation== '*'){
    alert(num1*num2)
}
else if(operation== '/'){
    alert(num1/num2)
}
